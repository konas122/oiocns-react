export const LoadBanner = (type: string) => {
  const names = window.location.hostname.split('.');
  if (names[0] === 'ai' && names[1] === 'zjhealthcloud') {
    names[0] = 'healthcloud';
  }
  let platformType = '';
  if (names.length > 0) {
    platformType = names[0];
  }
  if (!DefaultBannerData[platformType]) {
    platformType = 'orginone';
  }

  if (DefaultBannerData[platformType][type]) {
    return DefaultBannerData[platformType][type]['defaultImgList'];
  }
  return '';
};
